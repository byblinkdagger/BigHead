         * 还有一次，天空被一大面云蒙得紧紧的，却正好在头顶正上方的位置洞开一孔。于是，一汪巨大的圆形蓝天停止在那处，像是立刻会有湛蓝冰冷的液体倾泼下来。
         *
         * 还有那些深陷在碧绿山坡半腰上的羊道，纤细而深刻，十几条、几十条，甚至上百条并行蜿蜒，顺着山势音乐般熨帖地起伏扭转。整面山坡鼓荡着巨大而优美的力量。
         *
         * 还有暮归的山路上迎面遇到的一头牛，浑身漆黑，唯有额头正中嵌一块雪白的毛皮，呈完美的心形图案。
         *
         * 还有阴天里雨水初停的时刻。沼泽里的圆形叶片密密地挤生，每一片叶心都珍藏一颗完美精致的水珠。每一颗水珠都刻录了眼前完整的绿色世界。放眼望去，满眼明灿灿的绿意。又因为是阴天，无强光的反射，这绿意只郁结在低处，绿得欲罢不能。
         *
         * 还有很早就开始挤奶的那些傍晚时分，我赶着一头鼻子湿漉漉的小牛上山，看到黄衣的卡西帕亭亭玉立地站在视野高处的天空下，骑马的海拉提沿着山脊向她缓辔行去……在他俩身后，是一大团塞据了整面天空三分之一面积的云朵的侧面，像一座银子般徐徐放光的空中岛屿。
         *
         * 我亲眼所目睹的这些，与其说是自然的呈现物，不如说是遮蔽物。我过不去。高山巨壑。我并非缺少工具，也非时间不够，而是根本就没有入口，彻底没有入口。
         *
         * 对我来说，最寻常、最单调的日常生活也如大海般深不见底。斯马胡力赶羊时发出的各种吆喝声，羊能听懂，我却听不懂。班班认得自家的牛羊，若有别人家的牛靠近我家的盐槽，就吠叫着冲过去把它赶开。而我非得走近了仔细辨认烙在牲口耳朵上的标记。
         *
         * 我太过懦弱，无力承担。每当我面向一幕陌生而惊心的情景，举起相机——更像是躲藏在相机这样一个掩体之后。我不敢直视。像是一个说过谎的人。
         *
         * 所谓的“孤独感”，总是尴尬又悲伤的。然而不止这样，也不止是我。面对这样的时代，面对外部世界的喧嚣节奏，眼下这荒蛮之地的这支游牧民族又何尝不孤独呢？当我经过广阔无物的春秋牧场，经过一间局促简陋的泥土小屋，看到电视天线寂寞地伸向蓝天（那天线只是一根细长的木棍支起一张破旧的铝锅蒸篦）。我走进屋里，看到阴影中的人们紧围一台小小的黑白电视机（电源来自门外一块一尺见方的太阳能电池板）。我看到电视上布满雪花点，画面因信号不稳抖动不止。但还是能看清画面中展示的那个家庭极富有，家居富丽堂皇，庭院整齐考究，主人公清洁又悠闲。我又看到屏幕前所有的面孔都安静、认真，所有眼睛滋味无穷。年轻人向往着，年长者则惊奇而赞赏。这也是相机难以记录，无法说清的。
         *
         * 更多更宽广更强烈的冲击，是再偏远的角落、再执拗的心灵也无从回避的。流行哈萨克族歌曲中花哨的装饰音，年轻人服饰上夸张而无用的饰物，孩子香甜吸吮着的“娃哈哈”，深山小道边遗落的垃圾食品包装袋……世人都需平等地进入当下世界，无论多么坚牢的古旧秩序都正在被打开缺口。虽然从那个缺口进进出出的仍是传统事物，但每一次出入都有些许流失和轻微的替换。我感觉到了。
         *
         * 我在最细微的差异里的最深暗的裂隙中无边坠落。我的相机留不住任何一处路过的情景，而路过的情景，也没什么能挽留得住我。我不能停止这坠落。可循的线索如指纹般随时浮现，随时熄灭，无从把握。记在心里的，刚刚记住就立刻涣散。默念着的，念着念着就如嚼蜡般毫无意义。而四周确是现实的生活——确有食物在嘴中吞咽，确有大狗班班饥饿的追随，蒲公英确在耀眼地盛放。
         *
         * 是的，生活之河正在改道，传统正在往旧河床上一日日搁浅。外在的力量固然蛮横，但它强行制止所达到的效果远不及心灵的缓慢闭眼。老人们还没明白发生了什么事，年轻人就已经自若地接受了新的现实。这又有什么错呢？世间的心灵不都渴望着、追逐着更轻松更愉快的人生吗？谁能在整个世界前行的汪洋大潮中独自止步呢？牛羊数量正在剧增，牧人正在与古老的生产方式逐步告别——这场告别如此漫长。一点一滴地告别着。似乎以多长的时间凝聚成这样的生活，就得以多长的时间去消散。不会有陡然的变革，我们生活在匀速消散之中。匀速运动状态等于静止状态——这是最后的安慰。那么，还是先不要去可惜吧。还是先谅解了再说。先收起相机，把眼前的一切接受了再说……
         *
         * 我虽然带了移动硬盘和一大堆电池，但还是轻易不肯给大家拍照。小姑娘卡西帕整天哀求也没有用，小伙子斯马胡力一放羊回来就大喊：“李娟！那边又有一个地方！漂亮得很！”也没有用。
         *
         * 唯有当大家赶羊入栏时，剪羊毛时，擀毡时……忙得焦头烂额、啥都顾不上的时候，我才端起相机跑前跑后一顿猛拍。于是大家非常不乐意。因为那时候一个个又脏又累，有失形象。
         *
         * 偶尔在天气晴朗，大家悠闲又愉快的时候，我会主动提出为大家照相。于是所有人如过节一样快乐，纷纷换上最好的衣服往“漂亮的大石头”那边走。那块石头在林海孤岛的西南面的隘口边，又平又高，四面长满了爬山松，大家都很喜欢那里。
         *
         * 照相时，扎克拜妈妈必然会是叉着腰摆S曲线的。嫂子莎拉一定要光头的女孩加依娜站在左边，养子吾纳孜艾站在右边，一个也不能少。小伙子们则一定要和自己的马儿站在一起。拍合影时，哪怕画面分明宽宽绰绰，大家也一定要排作两排。并且一定要有蹲的有站的，个儿最高的一位一定会被拥着站在最中间。似乎合影的套路只能如此。此外，合影时大家一定要扁着嘴，丝毫不笑。似乎越严肃越气派。
         *
         * 一次进城时，我洗出了一部分照片带回家，把家里唯一的影簿插得满满当当。在后来的日子里，这本影簿在大家的日常生活中占据了多么重要的地位啊。平时它作为装饰品竖放在花毡边的木箱上。卡西帕哪怕只有三十秒的空闲，都要取下它匆匆翻看几页，再端正地摆回去。连揉面粉时都会将影簿摊开放在一旁，一边用力地揉，一边偏着脑袋细细揣摩，并不时指使路过的加依娜或觉约得别克帮忙翻过一页。扎克拜妈妈也常常流连其间，并且每次翻看都会有新发现：“呀，这里冒出一截班班的尾巴！”“呀！我的鞋子沾了牛粪！”每当家里来了客人，我们的影簿自然是招待客人的重要内容之一。如果客人上次已经看过一遍，下次来时，则会主动提出再看一遍。
         *
         * 脖子上挂着相机，一个人在无人的山谷里走啊走啊。迎面遇上的骑马人总会勒停马儿，大声向我问候。然后提出要我为其拍照。我同意后，他整整身上的衣服，扶正狐狸皮缎帽，肃容端坐马背，看向镜头。不知为何，那样的时候我极乐意做这件事。大约因为能顺从这个陌生人的意愿，能为他做些什么吧。于是“陌生”这个硬东西便变得服服帖帖的。总之那时我极殷勤，横的竖的正面的侧面的“啪啪啪”捏个不停，然后再回放一遍给他看。他骑在马上，俯向我的相机显屏仔细地看。看罢满意地道谢，然后与我告别。但不知为什么，他从来都不提“照片洗出来送我一张”之类的话。因此我实在不明白他为什么要向我道谢。
         *
         * 对于拍照这事，大多数时候我仍深感不自在。我没法令大家理解自己拍照的这一行为，也没法解释，似乎一解释就全都是谎言。我在这里生活，我的相机令我的介入成为“强行”的介入，令我与大家的相处形成某种对立状态。这种对立不公平、不自然，且不地道。当我举着相机对准别人时，总觉得像是举着枪对准了别人……不知这到底出自怎样的一种怪异心态。总之，我想留存大家的生活，到头来，却干扰了大家的生活。某种程度上，我使大家的生活成为表演。当我一举起相机，生活、劳动中的人们立刻调整坐姿，扯扯衣角，换了表情——作给外人看的，端庄而防备的表情。
         *
         * 虽然照相之前，我总会不辞辛苦套一番近乎。等大家说得高高兴兴，毫无防备的时候，再突然取出相机“咔嚓”一下子。但总是没用。大家速度总是比我快。镜头所到之处，总能迅速集合，排列成合影的标准队形。
         *
         * 是的，总是这样的——本来所有人好端端围坐一席，舒适地说笑、进食。我的相机一出现，亲亲热热的宴席转眼间就散了。大家把碗一推，忙乎起来。老奶奶掏出钥匙打开木箱，取出洗衣粉洗脸。主妇和女孩子纷纷跑到毡房后，换上出门做客时才穿的外套和鞋子。小伙子们大力擦皮鞋。唯有男主人矜持一些，顶多拉展身上外套，掸掸裤腿上的灰。但表情毫不含糊，绝对不笑。这相照得真没意思。
         *
         * 相机平添的其他烦恼就更多了。比方说，卡西帕对我的相机有浓烈的好奇心。好奇心本值得称赞，问题是这家伙还有更为彪悍的自信心，碰到啥问题都绝不轻易向我请教。于是，我在弹唱会上拍的好多精彩画面，回家没几天就被这家伙悄悄地、统统地删掉了……真是又心疼又难过。但怎么能指责这个小姑娘呢？而那些拍下的照片，又何尝真正属于过我。它们只是藉由我的相机凭空出现在这世上。如果我从不曾使用过这架相机，从不曾攫取过这些美妙瞬间，从不曾占有过这些画面。那伤心何来？像一个走了弯路的人，白白地辛苦，又无端地生气。
         *
         * 另外，自从相机坏了之后，大家都很生气，气我没本事修好它。若没相机的话，自然也就没有这么一茬责怨了。
